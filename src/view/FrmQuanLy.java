    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Thêm phần tử
        fillToTable();
        try {

            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập vào mã nhân viên!");
                txtId.requestFocus();
                return;
            }
            if (txtStaffName.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập vào tên nhân viên!");
                txtStaffName.requestFocus();
                return;
            }
            if (txtAge.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập tuổi nhân viên!");
                txtAge.requestFocus();
                return;
            }
            if (txtExperience.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập vào năm kinh nghiệm!");
                txtExperience.requestFocus();
                return;
            }
            //Check tuổi
            try {
                int tuoi = Integer.parseInt(txtAge.getText());
                if (tuoi < 0) {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập tuổi là số dương!");
                    txtAge.requestFocus();
                    return;
                }
                if (tuoi < 18) {
                    JOptionPane.showMessageDialog(this, "Chưa đủ tuổi lao động!");
                    txtAge.requestFocus();
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập tuổi là số!");
                txtAge.requestFocus();
                return;
            }
            //Check năm kinh nghiệm
            try {
                int namKN = Integer.parseInt(txtExperience.getText());
                if (namKN < 0) {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập năm kinh nghiệm lớn hơn hoặc bằng 0!");
                    txtExperience.requestFocus();
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập năm kinh nghiệm là số!");
                txtExperience.requestFocus();
                return;
            }
            //Check trùng id nhân viên
            for (NhanVien nv : lstNhanVien) {
                if (txtId.getText().equals(nv.getID())) {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên " + txtId.getText() + " đã tồn tại!");
                    txtId.requestFocus();
                    return;
                }
            }

            addNhanVien();
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công");
            fillToTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm nhân viên!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

