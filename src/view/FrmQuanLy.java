    private void fillToTable() {

        if (lstNhanVien == null) {
            JOptionPane.showMessageDialog(this, "Lỗi!");
        } else if (lstNhanVien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không có dữ liệu!");
        } else {
            model = (DefaultTableModel) tblQLNhanVien.getModel();
            model.setRowCount(0);
            for (NhanVien nv : lstNhanVien) {
                double luong = nv.getSalary();
                if (nv.getStatus(nv.getExperience() + "").equalsIgnoreCase("Bán chuyên")) {
                    luong = luong + 75;
                } else if (nv.getStatus(nv.getExperience() + "").equalsIgnoreCase("Chuyên nghiệp")) {
                    luong = luong + 100;
                } else {
                    luong = luong + 200;
                }
                model.addRow(new Object[]{
                    nv.getID(),
                    nv.getName(),
                    nv.getAge(),
                    nv.getExperience(),
                    nv.getRole(),
                    nv.getArea(),
                    luong,
                    nv.getStatus(nv.getExperience() + "")
                });
            }
        }

    }

    private void clearFrom() {
        txtId.setText("");
        txtStaffName.setText("");
        txtAge.setText("");
        txtExperience.setText("");
        cbbRole.setSelectedIndex(0);
        cbbArea.setSelectedIndex(0);
    }

    private void showDetail() {
        NhanVien nv = lstNhanVien.get(vitri);

        txtId.setText(nv.getID());
        txtStaffName.setText(nv.getName());
        txtAge.setText(nv.getAge() + "");
        txtExperience.setText(nv.getExperience() + "");
        cbbRole.setSelectedItem(nv.getRole());
        cbbArea.setSelectedItem(nv.getArea());
    }

    public void addNhanVien() {
        NhanVien nhanVien = new NhanVien();

        nhanVien.setID(txtId.getText());
        nhanVien.setName(txtStaffName.getText());
        nhanVien.setAge(Integer.parseInt(txtAge.getText()));
        nhanVien.setExperience(Integer.parseInt(txtExperience.getText()));
        nhanVien.setRole(cbbRole.getSelectedItem() + "");
        nhanVien.setArea(cbbArea.getSelectedItem() + "");

        lstNhanVien.add(nhanVien);
        model = (DefaultTableModel) tblQLNhanVien.getModel();
    }

